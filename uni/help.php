<p style = "font-size: 20px; margin-left: 1%">
     На странице мониторинга можно узнать информацию о ТС, 
     находящихся на территории на данный момент.
     Возможно узнать номер ТС, марку, модель, год выпуска, 
     время въезда на территорию, длительность нахождения ТС на ней.
     Если по номеру ТС не найдена информация о марке, модели и годе выпуска ТС, 
     будет выдано соответствующее сообщение в строке, соответствующей данному ТС. 
